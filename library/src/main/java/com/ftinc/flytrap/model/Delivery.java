package com.ftinc.flytrap.model;

import android.content.Context;
import android.os.Parcelable;

/**
 * Created by drew.heavner on 7/9/14.
 */
public abstract class Delivery implements Parcelable{

    /************************************************
     *
     * Constants
     *
     */

    public static final int TYPE_EMAIL = 0;
    public static final int TYPE_API = 1;

    /************************************************
     *
     * Variables
     *
     */

    /**
     * Called when a report is generated by the feedback flytrap view
     * to handle the delivery of the feedback to the developer in the method
     * that they
     *
     * @param report    the generated report
     */
    public abstract void onReportGenerated(Context ctx, Report report, OnReportHandler handler);

    /**
     * Return the type of delivery method of the class
     *
     * @return      the delivery type
     */
    public abstract int getType();


    @Override
    public int describeContents() {
        return 0;
    }

    /**
     * Interface to let the FlyTrap activity know when the
     * delivery system has finished handling the report
     */
    public static interface OnReportHandler{
        public void onFinish();
    }

}
